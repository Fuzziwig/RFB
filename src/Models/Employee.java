package Models;

public class Employee {
    private int employeeID;
    private int workplanID;
    private int addressID;
    private String employeeFirstName;
    private String employeeLastName;
    private String employeeTitle;
    private String employeePhoneNumber;
    private String email;

    public Employee() {
    }

    public Employee(int employeeID, int workplanID, int addressID, String employeeFirstName, String employeeLastName, String employeeTitle, String employeePhoneNumber, String email) {
        this.employeeID = employeeID;
        this.workplanID = workplanID;
        this.addressID = addressID;
        this.employeeFirstName = employeeFirstName;
        this.employeeLastName = employeeLastName;
        this.employeeTitle = employeeTitle;
        this.employeePhoneNumber = employeePhoneNumber;
        this.email = email;
    }

    public int getEmployeeID() { return employeeID; }

    public void setEmployeeID(int employeeID) { this.employeeID = employeeID; }

    public int getWorkplanID() { return workplanID; }

    public void setWorkplanID(int workplanID) { this.workplanID = workplanID; }

    public void setAddressID(int addressID) { this.addressID = addressID; }

    public int getAddressID() { return addressID; }

    public String getEmployeeFirstName() { return employeeFirstName; }

    public void setEmployeeFirstName(String employeeFirstName) { this.employeeFirstName = employeeFirstName; }

    public String getEmployeeLastName() { return employeeLastName; }

    public void setEmployeeLastName(String employeeLastName) { this.employeeLastName = employeeLastName; }

    public String getEmployeeTitle() { return employeeTitle; }

    public void setEmployeeTitle(String employeeTitle) { this.employeeTitle = employeeTitle; }

    public String getEmployeePhoneNumber() { return employeePhoneNumber; }

    public void setEmployeePhoneNumber(String employeePhoneNumber) { this.employeePhoneNumber = employeePhoneNumber; }

    public String getEmail() { return email; }

    public void setEmail(String email) { this.email = email; }
}

